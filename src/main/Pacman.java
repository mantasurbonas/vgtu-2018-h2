package main;

public class Pacman {
	int pacmanX = 5;
	int pacmanY = 5;

	public Pacman(int pacmanX, int pacmanY) {
		super();
		this.pacmanX = pacmanX;
		this.pacmanY = pacmanY;
	}

	public int getPacmanX() {
		return pacmanX;
	}

	public void setPacmanX(int pacmanX) {
		this.pacmanX = pacmanX;
	}

	public int getPacmanY() {
		return pacmanY;
	}

	public void setPacmanY(int pacmanY) {
		this.pacmanY = pacmanY;
	}

}
